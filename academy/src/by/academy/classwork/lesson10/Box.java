package by.academy.classwork.lesson10;

public class Box<T> {

	private T item;
	public int capacity;

	public Box() {
		this.capacity = capacity;
	}

	public T getItem() {
		return item;
	}

	public void setItem(T item) {
		this.item = item;
	}

	public int getCapacity() {
		return capacity;
	}

	public void setCapacity(int capacity) {
		this.capacity = capacity;
	}

}
